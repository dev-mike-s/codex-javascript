// Operatoren

// Arithmetische Operatoren
let summe = 5 + 3; // Addition
let differenz = 5 - 3; // Subtraktion
let produkt = 5 * 3; // Multiplikation
let quotient = 5 / 3; // Division
let rest = 5 % 3; // Modulo
let potenz = 5 ** 3; // Potenzierung

// Zuweisungsoperatoren
let wert = 10;
wert += 5; // Wert ist jetzt 15
wert -= 3; // Wert ist jetzt 12
wert *= 2; // Wert ist jetzt 24
wert /= 4; // Wert ist jetzt 6

// Vergleichsoperatoren
console.log(5 == "5"); // true (lose Gleichheit)
console.log(5 === "5"); // false (strikte Gleichheit)
console.log(5 != "5"); // false (lose Ungleichheit)
console.log(5 !== "5"); // true (strikte Ungleichheit)

// Logische Operatoren
let a = true;
let b = false;
console.log(a && b); // false (UND)
console.log(a || b); // true (ODER)
console.log(!a); // false (NICHT)

